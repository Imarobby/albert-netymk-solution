public class NestedLoops{
    public static void main(String[] commandLine){
        for(int i = Integer.parseInt(commandLine[0]) ; i<=5; i++)
         {  System.out.println();
			   for(int j = 1; j<=i; j++)
				       
					     
                    System.out.println("i = " + i + ", j = " + j);
					  }
    }
}
